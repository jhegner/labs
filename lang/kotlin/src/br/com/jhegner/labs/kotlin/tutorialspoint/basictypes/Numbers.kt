package br.com.jhegner.labs.kotlin.tutorialspoint.basictypes

fun main() {

    val a: Int = 1000
    val d: Double = 100.00
    val f: Float = 100.00f
    val l: Long = 10000000004
    val s: Short = 10
    val b: Byte = 1

    println("O seu valor Int eh: $a")
    println("O seu valor Double eh: $d")
    println("O seu valor Float eh: $f")
    println("O seu valor Long eh: $l")
    println("O seu valor Short eh: $s")
    println("O seu valor Byte eh: $b")
}